var komikLength = comic.length;
var thisElement2 = 0;

function innerContent2(content){
  $(".inner-link-komik").attr("src",comic[content].link);
};

$(".button-komik").click(function(e){
  e.preventDefault();
  thisElement2 = $(this).attr("data-obj");
  innerContent2(thisElement2);
});

$(document).ready(function(){
  var list = $("#list-komik .komik-card");
  var numToShow = 20;
  var button = $("#komik-lainnya");
  var numInList = list.length;
  list.hide();
  if (numInList > numToShow) {
    button.show();
  }
  list.slice(0, numToShow).show();

  button.click(function(){
      var showing = list.filter(':visible').length;
      list.slice(showing - 1, showing + numToShow).fadeIn();
      var nowShowing = list.filter(':visible').length;
      if (nowShowing >= numInList) {
        button.hide();
      }
  });
});