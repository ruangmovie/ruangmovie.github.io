$(".next-movie").click(function(e){
  e.preventDefault();
  if (thisElement2<dataLength-1) {
    thisElement2 = parseInt(thisElement2) + 1;
    innerContent2(thisElement2);
    dissBtn();
  };
});

$(".prev-movie").click(function(e){
  e.preventDefault();
  if (thisElement2>0) {
    thisElement2 = parseInt(thisElement2) - 1;
    innerContent2(thisElement2);
    dissBtn();
  };
});

//Button disable:
function dissBtn(){
  $(".prev-movie, .next-movie").removeClass("disabled");
  if (thisElement2<=0){
    $(".prev-movie").addClass("disabled");
  }
  else if (thisElement2>=dataLength-1){
    $(".next-movie").addClass("disabled");
  };
};

